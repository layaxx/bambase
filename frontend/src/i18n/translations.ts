type SetAtPath<T, Path extends unknown[], Value> = Path extends [infer K, ...infer Rest]
  ? K extends keyof T
    ? {
        [P in keyof T]: P extends K
          ? Rest extends []
            ? Value
            : SetAtPath<T[P], Rest, Value>
          : T[P]
      }
    : T
  : T

